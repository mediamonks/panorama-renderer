  public static getRotation(out: Quaternion, matm: Matrix4x4) {
    const mat = matm.m;
    let scaling = new Vector3();
    Matrix4x4.getScaling(scaling, matm);

    let is1 = 1 / scaling.x;
    let is2 = 1 / scaling.y;
    let is3 = 1 / scaling.z;

    let sm11 = mat[0] * is1;
    let sm12 = mat[1] * is2;
    let sm13 = mat[2] * is3;
    let sm21 = mat[4] * is1;
    let sm22 = mat[5] * is2;
    let sm23 = mat[6] * is3;
    let sm31 = mat[8] * is1;
    let sm32 = mat[9] * is2;
    let sm33 = mat[10] * is3;

    let trace = sm11 + sm22 + sm33;
    let S = 0;

    if (trace > 0) {
      S = Math.sqrt(trace + 1.0) * 2;
      out.w = 0.25 * S;
      out.x = (sm23 - sm32) / S;
      out.y = (sm31 - sm13) / S;
      out.z = (sm12 - sm21) / S;
    } else if (sm11 > sm22 && sm11 > sm33) {
      S = Math.sqrt(1.0 + sm11 - sm22 - sm33) * 2;
      out.w = (sm23 - sm32) / S;
      out.x = 0.25 * S;
      out.y = (sm12 + sm21) / S;
      out.z = (sm31 + sm13) / S;
    } else if (sm22 > sm33) {
      S = Math.sqrt(1.0 + sm22 - sm11 - sm33) * 2;
      out.w = (sm31 - sm13) / S;
      out.x = (sm12 + sm21) / S;
      out.y = 0.25 * S;
      out.z = (sm23 + sm32) / S;
    } else {
      S = Math.sqrt(1.0 + sm33 - sm11 - sm22) * 2;
      out.w = (sm12 - sm21) / S;
      out.x = (sm31 + sm13) / S;
      out.y = (sm23 + sm32) / S;
      out.z = 0.25 * S;
    }

    return out;
  }

